
def bytes_to_decimal(byte_str):
    decimal_values = [int(byte) for byte in byte_str]
    return decimal_values

def find_magic_word(data, magic_word):
    for idx, byte_str in enumerate(data):
        decimal_values = bytes_to_decimal(byte_str)
        if magic_word == decimal_values[:len(magic_word)]:
            return idx
    return None

